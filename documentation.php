<h2>NextGEN-Galleryview</h2>


<h3>Getting Started</h3>
<p>Simply use galleryview as your NextGen-Gallery template within your shortcode.</p>

<ol>
<li>Make sure <a href="//wordpress.org/plugins/nextgen-gallery/" target="_blank" rel="nofollow">NextGEN-Gallery</a> plugin is installed and activated.</li>
<li>Use the galleryview template within the ngg shortcode: <code>[nggallery id=1 template="galleryview"]</code></li>
</ol>

<p>Tip: While not required, this plugin default template is setup to support <a href="//jetpack.me/support/photon/" target="_blank" rel="nofollow">Jetpack Photon</a> for improved performance. </p>

<br />
<h3>Advanced Instructions</h3>
<p>For advanced users its recommended to create your own nextgen-gallery template. This will allow you to modify the template as needed. </p>

<ol>
<li>Create a folder called nggallery within your Wordpress theme.</li>
<li>Copy gallery-galleryview.php from the "templates" folder of the plugin and place it within the nggallery folder you created.</li>
<li>Now modify the template to your desires and use with NextGenGallery. Reference: <a href="//brandonhubbard.com/GalleryView/options.html" target="_blank" rel="nofollow">List of Galleryview Options</a></li>
<li>Use the galleryview template within the ngg shortcode: <code>[nggallery id=1 template="galleryview"]</code></li>
</ol>

<p>Note: You can have multiple types of galleryviews. When creating the file within your theme, simply rename "galleryview" to something else such as "mygalleryview". You can do this as many times as you wish just be sure the names used are always unique, then simply use it within a shortcode: <code>[nggallery id=1 template="mygalleryview"]</code></p>

